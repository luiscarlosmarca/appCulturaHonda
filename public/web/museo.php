					<p align="justify">La vieja casona donde vivieron Don Pedro Aquilino López Medina y su Señora Rosario Pumarejo Cote, se había destinado al comercio y las habitaciones que daban a la calle, servían de local a la ferretería de Don Manuel José Bonilla y al almacén de su hijo, Don Efraín Bonilla.

El inmueble, cuyo primer dueño fue el Capitán Bernardo Botero y el segundo Manuel José Bonilla, adquirido por la Nación y convertido en la Casa Cultural "ALFONSO LOPEZ PUMAREJO"; adquirida bajo la administración de la Dirección de Inmuebles Nacionales del Ministerio de Obras Publicas, fue objeto entonces de una cuidadosa reconstrucción con materiales y elementos apropiados que incorporaron a la mayoría de los muros centenarios de cal y canto. Testigo fiel del paso de varias generaciones, es hoy escenario de una recordada época colonial colombiana.

Edificación representativa de la arquitectura colonial de clásico andaluz cuya construcción data del siglo XVII, se desarrolla en una sola planta, con cubierta en teja de barro, compuesta a cuatro aguas y patio central, construida en adobe, con sistema portante de muros de carga, canales y bajantes a la vista y vistosas columnas de madera fina.
</p>
