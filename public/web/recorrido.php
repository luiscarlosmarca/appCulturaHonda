						Saliendo de la plaza de mercado se toma la cuesta de San Francisco, hasta llegar a la esquina de la carrera 13 con calle
						11, definida por un inmueble de dos pisos, con pies derechos, zócalo y balaustras azules. Avanznado por la calle 11
						se llega al <b>paseo Bolivar </b>, identificado por bancas y un busto de libertador. <br>

						Saliendo del parque José León Armero, hacia el oriente, se llega al barrio Alto del Rosario. Recorrer
						sus calles significa encontrarse con casas coloniales muy bien conservadas.
									
